const int MAXN = 77777;

typedef long long int ll;

class TheSquareRootDilemma {
    public:
        int countPairs(int N, int M) {
            vector<ll> sq;
            for(int i=1; i<=MAXN; i++){
                ll tmp = i;
                tmp *= i;
                sq.push_back(tmp);
            }
            
            int sz = sq.size();
            int cnt = 0;
            for(int i=0; i<sz; i++){
                ll A = sq[i];
                if(A>N)break;
                for(int j=0; j<sz; j++){ 
                    ll B = sq[j];
                    if(B>M)break;
                    cnt += 1;
                    cout<<"now "<<A<<" "<<B<<endl;
                }
            }
            return cnt;
        }
};

