	SequencePermutation * obj = new SequencePermutation();
	clock_t start = clock();
	int my_answer = obj->determineConfigurations(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 6;
