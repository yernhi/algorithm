class TheLuckyNumbers {
    public:
            int count(int, int);
};
vector <long long> values;
void dfs(long long cur){
    if(cur*10>=1000000000){
        return ;
    }
    long long t1 = cur*10+4;
    long long t2 = cur*10+7;
    values.push_back(t1);
    values.push_back(t2);
    dfs(t1);
    dfs(t2);
}


int TheLuckyNumbers::count(int a, int b) {
    values = vector <long long >();
    dfs(0);
    sort(values.begin(), values.end());
    vector <long long>::iterator left, right,tt;
    left = lower_bound(values.begin(), values.end(), a);
    right = lower_bound(values.begin(), values.end(), b);
    if(*right!=b){
        right--;
    }
   
    //cout<<"left values is "<<values[left-values.begin()]<<endl;
    //cout<<"right values is "<<values[right-values.begin()]<<endl;
    return right-left+1;
}

