class FallingFactorialPower {
    public:
            double compute(int, int);
};

double FallingFactorialPower::compute(int n, int k) {
    double ans = 1;
    if(k>0){
        for(int i=0; i<=k-1; i++){
            ans*=(n-i);
        }
        return ans;
    }else{
        k=k*-1;
        for(int i=1; i<=k; i++){
            ans/=(n+i);
        }
        return ans;
    }


}
